     */
    public function index()
    {
            $city = 'Anuradhapura';
            $lowm = new LaravelOWM();
            $current_weather = $lowm->getCurrentWeather($city);
            $weather = $current_weather->temperature->now->getValue();
            return view('weather')
                ->withCity($city)
                ->withWeather($weather);

    }

